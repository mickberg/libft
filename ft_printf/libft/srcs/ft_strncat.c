#include <stdlib.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *s1_tmp;

	s1_tmp = s1;
	while (*s1_tmp != '\0')
		++s1_tmp;
	while (*s2 != '\0' && n--)
	{
		*s1_tmp = *s2;
		++s1_tmp;
		++s2;
	}
	*s1_tmp = '\0';
	return (s1);
}
