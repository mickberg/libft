#include "libft.h"

int		ft_intlen(int nb)
{
	long num;
	int len;

	num = ft_abs(nb);
	if (num == 0)
		return (1);
	len = 0;
	while (num > 0)
	{
		num /= 10;
		++len;
	}
	return (len);
}
