#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char	*d;

	d = dst;
	while (*src != '\0' && len-- > 0)
		*d++ = *src++;
	while (*d != '\0')
		*d++ = '\0';
	return (dst);
}
