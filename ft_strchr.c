#include <stdlib.h>

char	*ft_strchr(const char *s, int c)
{
	while (*s != '\0')
	{
		if ((int)*s == c)
			return ((char*)s);
		++s;
	}
	if (c == '\0')
		return ((char*)s);
	return (NULL);
}
